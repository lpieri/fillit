#include "fillit.h"

int				ft_sqrt_up(t_tetris **matrix)
{
	int		nb_p;
	int		i;

	nb_p = 0;
	while (*matrix++)
		nb_p++;
	i = 1;
	while (i * i < 4 * nb_p)
		i++;
	return (i);
}
